    //derive gpa for each course
    private double getGrade(String str) {
        double d = Double.parseDouble(str);

        if (d >= 80) {
            return 4.00;
        } else if (d >= 75) {
            return 3.75;
        } else if (d >= 70) {
            return 3.50;
        } else if (d >= 65) {
            return 3.25;
        } else if (d >= 60) {
            return 3.00;
        } else if (d >= 55) {
            return 2.75;
        } else if (d >= 50) {
            return 2.50;
        } else if (d >= 45) {
            return 2.25;
        } else if (d >= 40) {
            return 2.00;
        } else {
            return 0.00;
        }
    }
    //Calculte cgpa for the semester
    private String cgpaCalculation() {

        double d1 = getGrade(marksub1.getText());
        double d2 = getGrade(marksub2.getText());
        double d3 = getGrade(marksub3.getText());
        double d4 = getGrade(marksub4.getText());
        double d5 = getGrade(marksub5.getText());
        double d6 = getGrade(marklab1.getText());
        double d7 = getGrade(marklab2.getText());
        double d8 = getGrade(marklab3.getText());
        double d9 = getGrade(marklab4.getText());

        double result = (d1 * 3 + d2 * 3 + d3 * 3 + d4 * 3 + d5 * 3 + d6 * 1.5 + d7 * 1.5 + d8 * 1.5 + d9 * 0.75) / 20.25;
        String res2 = Double.toString(result);
        String show = res2.substring(0, 3);
        return show;
    }

